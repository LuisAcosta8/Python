#MVC Dividir cualquier aplicacion en tre grandes modulos
##Modelo: Gestionar datos
##Vista: Modulo encargado de mostrar informacion al usuario 
##Controlador: Gestiona comunicaciones entre modelo-vista

#1. Peticion del usuario a la vista
#2. Dicha peticion es registrada por el controlador
#3. El controlador demanda datos al modelo
#4. El modelo envia datos al controlador
#5. El controlador los envia a la vista
#6. La vista muestra estos datos al usuario

